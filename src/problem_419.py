def transition_matrix():
    matrix = np.zeros((len(ATOMS), len(ATOMS)), dtype=np.uint64)
    for source, decay in enumerate(DECAYS):
        for target in decay:
            matrix[target, source] += 1
    return matrix


def matrix_product(a, b):
    result = np.zeros_like(a)
    for begin in range(0, len(ATOMS), 8):
        end = min(begin + 8, len(ATOMS))
        result += a[:, begin:end] @ b[begin:end, :]
        result %= MOD
    return result


def matrix_vector_product(matrix, vector):
    result = np.zeros_like(vector)
    for begin in range(0, len(ATOMS), 8):
        end = min(begin + 8, len(ATOMS))
        result += matrix[:, begin:end] @ vector[begin:end]
        result %= MOD
    return result


def atom_counts(n):
    counts = np.zeros(len(ATOMS), dtype=np.uint64)
    for atom in (71, 49):
        counts[atom] += 1
    matrix = transition_matrix()
    exponent = n - 8
    while exponent:
        exponent, remainder = divmod(exponent, 2)
        if remainder:
            counts = matrix_vector_product(matrix, counts)
        if exponent:
            matrix = matrix_product(matrix, matrix)
    return counts


def digit_counts(n):
    counts = atom_counts(n)
    digits = np.array(
        [[atom.count(digit) for atom in ATOMS] for digit in "123"],
        dtype=np.uint64,
    )
    return tuple(int(value % MOD) for value in digits @ counts)


